    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        
        Cuentas misDatos = new Cuentas();
        if(!misDatos.validarUsuario(nombre_cuenta.getText(), new String (clave.getPassword()))){
            JOptionPane.showMessageDialog(rootPane, "Usuario o clave incorrecta");
            nombre_cuenta.setText("");
            clave.setText("");
            nombre_cuenta.requestFocusInWindow();
            return;
        }
        appBanco miApp = new appBanco();        
         this.setVisible(false);
         miApp.setLocationRelativeTo(null);
         miApp.setVisible(true);
         this.dispose();
        
    }//GEN-LAST:event_loginActionPerformed

